STMusic         lbra    Music
MInit           orcc    #$10
                lda     #$3F
                sta     MSwitch
                orcc    #$50
                leax    WFTable,pcr
                stx     MVoice1
                stx     MVoice2
                stx     MVoice3
                stx     MVoice4
                leax    NoteStart,pcr
                stx     Muwork
                rts        

ReadMusic       ldx     Muwork
                lda     ,x+
                sta     MDur
                rts        

Notes           leay    NTable,pcr
                lda     ,x+
                ldu     a,y
                stu     MInc1
                lda     ,x+
                ldu     a,y
                stu     MInc2
                lda     ,x+
                ldu     a,y
                stu     MInc3
                lda     ,x+
                ldu     a,y
                stu     MInc4
                stx     Muwork
                rts

MPlay           ldy     #MTempo
MPlay1          lda     [MVoice1]
                adda    [MVoice2]
                adca    [MVoice3]
                adca    [MVoice4]
                sta     MPort
                ldd     MVoice1+1
                addd    MInc1
                std     MVoice1+1
                ldd     MVoice2+1
                addd    MInc2
                std     MVoice2+1
                ldd     MVoice3+1
                addd    MInc3
                std     MVoice3+1
                ldd     MVoice4+1
                addd    MInc4
                std     MVoice4+1
                leay    ,-y
                bne     TimeWaste
                dec     MDur
                beq     EndNote
                bra     MPlay

TimeWaste       lda     ,x
                bra     Waste1
Waste1          bra     Waste2
Waste2          bra     Waste3
Waste3          bra     MPlay1
EndNote         rts

Music           lbsr    MInit
MLoop           lbsr    ReadMusic
                cmpa    #0
                bne     GetNotes
                rts

GetNotes        lbsr    Notes
                bsr     MPlay
                bra     MLoop

